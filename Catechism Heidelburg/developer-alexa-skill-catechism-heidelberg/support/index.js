exports.handler = function(event, context, callback) {
    var alexa = Alexa.handler(event, context);
    alexa.APP_ID = APP_ID;
    alexa.registerHandlers(handlers);
    alexa.execute();
};

var handlers = {
    'LaunchRequest': function () {
        this.emit('AMAZON.HelpIntent');
    },
    'HCIntent': function () {
		var question = this.event.request.intent.slots.question.value;

        var error = '';

        if (!question) {
            error = "I did not hear what question you wanted to hear.";
            this.emit(':tellWithCard', error, "Heidelburg Catechism", error);
            return;
        }

        if (isNaN(question)) {
            error = "I did not understand what question you wanted to hear.";
            this.emit(':tellWithCard', error, "Heidelburg Catechism", error);
            return;
        }

		if (question < 1 || question > 128) {
            error = "Please ask for a question between one and one hundred and twenty eight.";
            this.emit(':tellWithCard', error, "Heidelburg Catechism", error);
            return;
		}

		var question_data = hc[question - 1];

        var speech = "Question. " + question_data.question + " Answer. " + question_data.answer;

        this.emit(':tellWithCard', speech, "Heidelburg Catechism", speech);
    },
    'AMAZON.HelpIntent': function () {
        this.emit(':ask', "You can say, what is question one, or, tell me question one", "What can I help you with?");
    },
    'AMAZON.StopIntent': function () {
        this.emit(':tell', "OK!");
    },
    'AMAZON.CancelIntent': function () {
        this.emit(':tell', "OK!");
    }
};
